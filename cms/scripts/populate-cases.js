function postCase(c, idx) {
  return new Promise((resolve) => {
    const bodyStr = JSON.stringify({ data: c });
    const req = http.request({
      hostname: host,
      port: port,
      path: '/api/cases',
      method: 'POST',
      headers: {
        'Authorization': 'Bearer ' + token,
        'Content-Type': 'application/json',
        'Content-Length': Buffer.byteLength(bodyStr)
      }
    }, (res) => {
      let d = '';
      res.on('data', c => d += c);
      res.on('end', () => {
        try {
          const j = JSON.parse(d);
          if (j.error) {
            console.log(`❌ [${idx + 1}] ${c.title.substring(0, 20)} — ${j.error.message}`);
          } else {
            console.log(`✅ [${idx + 1}] ${c.title.substring(0, 30)}`);
          }
        } catch (e) {
          console.log(`❌ [${idx + 1}] ${c.title.substring(0, 20)} — 解析失败`);
        }
        resolve();
      });
    });
    req.on('error', () => {
      console.log(`❌ [${idx + 1}] ${c.title.substring(0, 20)} — 連接失敗，請確認 Strapi 運行在 port ${port}`);
      resolve();
    });
    req.write(bodyStr);
    req.end();
  });
}

